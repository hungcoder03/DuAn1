   KhachHang readForm() {
    String maKH = txt_tt_Makhachhang.getText().trim();
    String hoTen = txt_tt_Tenkhachhang.getText().trim();
    String SDT = txt_tt_SDTkhachhang.getText().trim();
    String mail = txt_tt_Mailkhachhang.getText().trim();
    String diaChi = txt_tt_Diachikhachhang.getText().trim();
    
    Date ngayTao = new Date();
    int namSinh = 0;
    try {
        namSinh = Integer.parseInt(txtNamSinh.getText().trim());
    } catch (NumberFormatException e) {
        namSinh = 0; // Đặt giá trị mặc định nếu không chuyển đổi được
    }

    boolean gt = radio_TT_Nam.isSelected();
    boolean tinhTrang = radio_tt_hoatdong.isSelected();
    
    // Kiểm tra email
    String emailPattern = "^[a-zA-Z0-9._%+-]{6,}@[a-zA-Z0-9.-]{2,}\\.[a-zA-Z]{1,}$";
    if (!mail.matches(emailPattern)) {
        JOptionPane.showMessageDialog(this, "Email không hợp lệ. Vui lòng nhập đúng định dạng!");
        txt_tt_Mailkhachhang.setBackground(Color.PINK); // Đổi màu nền thành hồng
        return null;  // Nếu email không hợp lệ, trả về null
    } else {
        txt_tt_Mailkhachhang.setBackground(Color.WHITE); // Khôi phục lại màu nền
    }

    return new KhachHang(maKH, hoTen, gt, namSinh, SDT, mail, diaChi, ngayTao, tinhTrang);
}



