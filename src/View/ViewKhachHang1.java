    private void btn_tt_suaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_tt_suaActionPerformed
        // TODO add your handling code here:
        // Lấy chỉ số dòng khách hàng được chọn
        // Lấy chỉ số dòng khách hàng được chọn
    int i = tbl_khachHang.getSelectedRow();

    if (i < 0) {
        // Nếu không chọn dòng nào
        JOptionPane.showMessageDialog(this, "Bạn chưa chọn dòng để sửa");
    } else {
        // Xác nhận việc sửa
        int chon = JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn sửa không?");
        if (chon == 0) {
            String maKh = tbl_khachHang.getValueAt(i, 0).toString();

             // Kiểm tra Mã khách hàng không được trống
            if (maKh.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Mã khách hàng không được để trống.");
                tbl_khachHang.setBackground(Color.PINK); // Đổi màu nền thành hồng cho bảng (hoặc trường mã khách hàng nếu là JTextField)
                return;
            } else {
                tbl_khachHang.setBackground(Color.WHITE); // Khôi phục lại màu nền nếu hợp lệ
            }
            
            // Lấy thông tin từ form
            KhachHang kh = readForm();

            if (kh == null) {
                // Nếu readForm trả về null (khi có lỗi trong kiểm tra)
                return;
            }

            String sdt = kh.getSoDT();
            String email = kh.getMail();
            String hoTen = kh.getHoTen();
            int namSinh = kh.getNamSinh();

            // Kiểm tra Số điện thoại hợp lệ (10 chữ số, bắt đầu bằng 0 và không có ký tự đặc biệt)
            if (sdt.isEmpty() || !sdt.matches("^0[0-9]{9}$")) {
                JOptionPane.showMessageDialog(this, "Số điện thoại phải là 10 chữ số và bắt đầu bằng 0.");
                txt_tt_SDTkhachhang.setBackground(Color.PINK); // Đổi màu nền thành hồng
                return; // Nếu số điện thoại không hợp lệ, dừng lại
            } else {
                txt_tt_SDTkhachhang.setBackground(Color.WHITE); // Khôi phục lại màu nền nếu hợp lệ
            }

            // Kiểm tra tên khách hàng không rỗng
            if (hoTen.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Họ tên không được để trống.");
                txt_tt_Tenkhachhang.setBackground(Color.PINK); // Đổi màu nền thành hồng
                return;
            } else {
                txt_tt_Tenkhachhang.setBackground(Color.WHITE); // Khôi phục lại màu nền nếu hợp lệ
            }

            // Kiểm tra năm sinh hợp lệ
            if (namSinh < 1950 || namSinh > 2010) {
                JOptionPane.showMessageDialog(this, "Năm sinh không được bỏ trống và năm sinh phải trong khoảng từ 1950 đến 2010.");
                txtNamSinh.setBackground(Color.PINK); // Đổi màu nền thành hồng
                return;
            } else {
                txtNamSinh.setBackground(Color.WHITE); // Khôi phục lại màu nền nếu hợp lệ
            }

            // Kiểm tra email hợp lệ
            String emailPattern = "^[a-zA-Z0-9._%+-]{6,}@[a-zA-Z0-9.-]{2,}\\.[a-zA-Z]{1,}$";
            if (!email.matches(emailPattern)) {
                JOptionPane.showMessageDialog(this, "Email không hợp lệ. Vui lòng nhập đúng định dạng!");
                txt_tt_Mailkhachhang.setBackground(Color.PINK); // Đổi màu nền thành hồng
                return;
            } else {
                txt_tt_Mailkhachhang.setBackground(Color.WHITE); // Khôi phục lại màu nền nếu hợp lệ
            }

            // Tiến hành sửa thông tin khách hàng
            rp.suaThongTinKH(maKh, kh);
            JOptionPane.showMessageDialog(this, "Sửa thông tin thành công");

            // Cập nhật lại bảng
            this.fillTable(rp.getAllKhachHang(null, null));
            showData(i); // Hiển thị lại dữ liệu mới cho dòng đã chọn
        } else {
            JOptionPane.showMessageDialog(this, "Bạn đã hủy sửa");
        }
    }
    }//GEN-LAST:event_btn_tt_suaActionPerformed

